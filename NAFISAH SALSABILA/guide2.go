package main

import "fmt"

func main() {
    var x rune
    fmt.Scanf("%c", &x)

    if (x >= 'a' && x <= 'z') || (x >= 'A' && x <= 'Z') {
        if x == 'a' || x == 'i' || x == 'u' || x == 'e' || x == 'o' ||
           x == 'A' || x == 'I' || x == 'U' || x == 'E' || x == 'O' {
            fmt.Println("vokal")
        } else {
            fmt.Println("konsonan")
        }
    } else {
        fmt.Println("bukan huruf")
    }
}
